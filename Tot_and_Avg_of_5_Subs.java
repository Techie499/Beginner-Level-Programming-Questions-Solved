class Tot_and_Avg_of_5_Subs{
    static void main(String args[]){
        int phy=72;
        int bio=84;
        int chem=74;
        int eng=64;
        int maths=79;
        
        int tot=phy+bio+chem+eng+maths;
        double avg=tot/5;
        System.out.println("Total marks : "+tot);
        System.out.println("Average Marks : "+avg);
    }
}        